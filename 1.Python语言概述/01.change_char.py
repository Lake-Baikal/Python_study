# \t 制表符 一个制表位，实现对齐功能,相当于输出tab键
print("jake\t20")
# \n 换行
print("Hello,jack\nHello,tom")
# \\输出斜杠
print("D:\\\\python\\\\chapter02")
# \"输出双引号
print('杨过说:\"小龙女，嫁给我吧\"')
# \'输出单引号
print("郭靖说:\'hello\'")
# \r 输出回车 ,会定位到第一段，覆盖第一段。
print("段誉向西行,\r他虽不会武功")

print("姓名\t年龄\t籍贯\t住址\t\nJone\t12\t河北\t北京")


